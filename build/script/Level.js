class Level {
  constructor({ map, width, height, position, baseSize }) {
    this._width = width;
    this._height = height;
    this._position = {
      x: position.x,
      y: position.y,
    };
    this._baseSize = {
      width: baseSize.width,
      height: baseSize.height,
    };
    this._bricks = this._setBricks(map);
  }

  _setBricks(map) {
		return map
      .map((row, y) => {
        return row
          .map((key, x) => key ? new Brick({ ...Brick.types[key], coords: { x, y }, baseSize: this._baseSize, levelPosition: this._position }) : null)
          .filter(brick => brick)
      })
      .flat();
  }

  _setBricksSize() {
    this._bricks.forEach(brick => {
      brick.setSize({
        baseSize: this._baseSize,
        levelPosition: this._position,
      })
    });
  }

  setSize({ width, height, position, baseSize }) {
    this._width = width;
    this._height = height;
    this._position.x = position.x;
    this._position.y = position.y;
    this._baseSize.width = baseSize.width;
    this._baseSize.height = baseSize.height;
    this._setBricksSize();
  }

  getBricks() {
    return this._bricks;
  }

}

